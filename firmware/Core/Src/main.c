// ======= my_flush_cb =======

void my_flush_cb(lv_display_t * display, const lv_area_t * area, uint8_t * px_map)
{
    uint32_t w = (uint32_t)(area->x2 - area->x1 + 1);
    uint32_t h = (uint32_t)(area->y2 - area->y1 + 1);

    if (s_lcd_flush.busy) {
        // shouldn't happen with proper LVGL usage
        return;
    }

    s_lcd_flush.busy    = true;
    s_lcd_flush.display = display;
    s_lcd_flush.area    = *area;
    s_lcd_flush.w       = w;
    s_lcd_flush.h       = h;
    s_lcd_flush.line    = 0;
    s_lcd_flush.src     = (uint16_t *)px_map;

    setAddrWindow(area->x1, area->y1, area->x2, area->y2);

    DC_DATA();
    CS_A();

    lcd_dma_start_next_line();

}

// ======= Helper =======

static void lcd_dma_start_next_line(void)
{
    if (!s_lcd_flush.busy) return;

    if (s_lcd_flush.line >= s_lcd_flush.h) {
        CS_D();
        s_lcd_flush.busy = false;
        lv_display_flush_ready(s_lcd_flush.display);
        return;
    }

    uint32_t w    = s_lcd_flush.w;
    uint32_t line = s_lcd_flush.line;

    uint16_t *src = s_lcd_flush.src + (line * w);
    uint8_t  *dst = lcd_line_buf;

    for (uint32_t x = 0; x < w; x++) {
        uint16_t c = *src++;

        uint8_t r5 = (c >> 11) & 0x1F;
        uint8_t g6 = (c >> 5)  & 0x3F;
        uint8_t b5 =  c        & 0x1F;

        *dst++ = r5 << 3;
        *dst++ = g6 << 2;
        *dst++ = b5 << 3;
    }

    if (HAL_SPI_Transmit_DMA(&hspi2, lcd_line_buf, w * 3) != HAL_OK) {
        CS_D();
        s_lcd_flush.busy = false;
        lv_display_flush_ready(s_lcd_flush.display);
    }
}

// ======= DMA complete callback =======

void HAL_SPI_TxCpltCallback(SPI_HandleTypeDef *hspi)
{
    if (hspi->Instance == SPI2) {
        if (s_lcd_flush.busy) {
            s_lcd_flush.line++;
            lcd_dma_start_next_line();
        }
    }
}


