// ===== Main Loop function ====
void mainLoop()
{
	while (1)
	{
		if (swipeLock.magneticCardReader.debounce && (int32_t) HAL_GetTick() - swipeLock.magneticCardReader.previousDebounceTick >= 500) // 500 ms debounce
			swipeLock.magneticCardReader.debounce = false;
		tryReadMagneticCardReaderData(&swipeLock.magneticCardReader);

		// update the startup display until we move to vacant screen
		if (!display_startup_done) {
			update_startup();
		}

		lv_timer_handler_run_in_period(5); /* run lv_timer_handler() every 5ms */
//		HAL_Delay(3);
	}
}

